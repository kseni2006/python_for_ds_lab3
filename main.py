import numpy as np

# 1. Створюємо одновимірний масив з 200 випадкових чисел від -100 до 100
arr = np.random.randint(-100, 101, size=200)
print("1. Початковий масив (фрагмент перших 15 елементів):\n", arr[:15])

# 2. Використовуючи маску, відфільтровуємо всі додатні числа в масиві
positive_mask = arr > 0
positive_numbers = arr[positive_mask]
print(f"\n2. Знайдено додатних чисел: {len(positive_numbers)}")
print("   Фрагмент додатних чисел:\n  ", positive_numbers[:15])

# 3. Замінюємо всі від’ємні значення на нулі
arr[arr < 0] = 0
print("\n3. Масив після заміни від'ємних на нулі (фрагмент):\n", arr[:15])

# 4. Обчислюємо середнє значення отриманого масиву
mean_value = np.mean(arr)
print("\n4. Середнє значення отриманого масиву:", mean_value)